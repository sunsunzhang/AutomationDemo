package dataType;

public class Post {
	public int postId;
	public int id;
	public String name;
	public String email;
	public String body;
	
	public int getPostId() {
		return postId;
	}

	public int getid() {
		return id;
	}

	public String getName() {
		return name;
	}

	public String getEmail() {
		return email;
	}

	public String getBody() {
		return body;
	}

}
